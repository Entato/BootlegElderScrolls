public class Hero{
    //fields
    private int health;
    private int defence;
    private int magicResist;
    private int evasion;
    private int speed;
    private String name;
    
    //Hero constructor
    public Hero(int health, int defence, int magicResist, int evasion, int speed, String name){
        this.health = health;
        this.defence = defence;
        this.magicResist = magicResist;
        this.evasion = evasion;
        this.speed = speed;
        this.name = name;
    }
    
   
    
    
}